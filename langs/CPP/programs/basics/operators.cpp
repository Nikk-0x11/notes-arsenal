#include <iostream>
using namespace std;

int main()
{
  int a = 17;
  char b = 'N';
  bool c = true;
  double d = 3.14;
  string e = "some random text.";

  cout << "  integer -> " << a << endl;
  cout << "character -> " << b << endl;
  cout << "  boolean -> " << c << endl;
  cout << "   double -> " << d << endl;
  cout << "   string -> " << e << endl;
  return 0;
}
